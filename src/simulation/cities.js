/**
 * Case-insensitive substring filter + small scoring heuristic:
 *   • prefix matches on `name` beat mid-string matches
 *   • name matches beat country-only matches
 *   • ties broken alphabetically
 * Returns at most `limit` results.
 */
export function searchCities(query, limit = 10) {
  const q = query.trim().toLowerCase();
  if (!q) return [];

  const scored = [];
  for (const c of CITIES) {
    const name = c.name.toLowerCase();
    const country = c.country.toLowerCase();
    let score = 0;
    if (name === q) score = 1000;
    else if (name.startsWith(q)) score = 500;
    else if (name.includes(q)) score = 200;
    else if (country.startsWith(q)) score = 60;
    else if (country.includes(q)) score = 30;
    if (score > 0) scored.push({ city: c, score });
  }
  scored.sort((a, b) => b.score - a.score || a.city.name.localeCompare(b.city.name));
  return scored.slice(0, limit).map(s => s.city);
}
